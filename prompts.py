# coding=utf-8


base_prompt = (
    "【系统任务】\n"
    "你是一个擅长处理复杂问题的 AI 助手。为了高效解决用户的以下问题，"
    "我们需要采用“思维骨架”（Skeleton-of-Thought）的方法：先规划结构，再并行填充内容。\n\n"
    "【用户问题】\n"
    "{user_question}\n\n"
)


skeleton_trigger_zh = (
    "【步骤一指令：分析与规划】\n"
    "请首先对任务进行判断：\n"
    "1. **直接回答模式**：如果满足以下任一条件，请**直接输出最终答案**，禁止使用 '####' 格式：\n"
    "   - 问题很简单（如知识问答、简短查询）。\n"
    "   - 任务具备强烈的逻辑顺序依赖（步骤B必须等待步骤A的结果），无法平行处理。\n"
    "2. **骨架拆分模式**：只有当任务复杂且适合**并行处理**（如多工具和智能体调用、多角度分析、多方面列举、长文写作规划）时，才按以下格式输出骨架：\n"
    "   - 每个分支独立一行，以 '####' 开头。\n"
    "   - 后跟简短的【分支标题】，以英文冒号 ':' 结尾。\n"
    "   - 冒号后必须紧跟省略号 '...'，禁止在此生成内容。\n"
    "   - 结尾输出 '####%%%%'。\n"
    "   - 示例：\n"
    "     ####分支一:...\n"
    "     ####分支二:...\n"
    "     ####%%%%\n\n"
    "【开始执行（直接回答或输出骨架）】：\n"
)

parallel_trigger_zh = (
    "【步骤二指令：内容填充】\n"
    "我们已经制定了该问题的整体回答骨架如下：\n"
    "{skeleton_context}\n\n" # <--- 步骤一生成的骨架，全局规划
    "你现在的具体任务是撰写其中一个分支的内容。\n"
    "当前任务分支：【 {current_point} 】\n\n"
    "严格约束：\n"
    "1. 直接开始撰写该分支的正文内容。\n"
    "2. 禁止输出标题或“####”，禁止输出开场白。\n"
    "3. 内容需独立完整，逻辑上能衔接整体骨架。\n\n"
    "【直接开始回答】：\n"
)


















